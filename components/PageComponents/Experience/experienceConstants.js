export const getStatusBg = (status) => {
  switch (status) {
    case "completed":
      return "bg-blue-900 border-accent";
    case "current":
      return "bg-red-900 border-accent";
    case "upcoming":
      return "bg-yellow-500/20 border-accent";
    default:
      return "bg-gray-500/20 border-accent";
  }
};
